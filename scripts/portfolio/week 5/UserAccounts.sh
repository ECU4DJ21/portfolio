#!/bin/bash

#format /etc/passwd into a neat table using awk
# use in cmd $ cat /etc/passwd | UserAccounts.sh

awk 'BEGIN {

    FS=":";
    print "| \033[34mUsername\033[0m           | \033[34mUserID\033[0m | \033[34mGroupID\033[0m | \033[34mHome\033[0m                             | \033[34mShell\033[0m           |";
    print "|____________________|________|_________|__________________________________|_________________|" 
}
/bash$/{ 

    printf ("| \033[33m%-18s\033[0m | \033[35m%-6s\033[0m | \033[35m%-7s\033[0m | \033[35m%-32s\033[0m | \033[35m%-15s\033[0m |\n", $1, $3, $4, $6, $7);
}
END {

      print "|____________________|________|_________|__________________________________|_________________|";

}' 